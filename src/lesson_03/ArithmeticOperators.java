package lesson_03;

public class ArithmeticOperators {
    public static void main(String[] args) {

        int a = 20;
        int b = 3;

        double c = 20.0;
        double d = 3.0;


        System.out.println("a + b = " +(a + b));
        System.out.println("a - b = " +(a - b));
        System.out.println("a * b = " +(a * b));
        System.out.println("a / b = " +(a / b));
        System.out.println("a % b = " + (a % b));
        System.out.println("c / d = " +(c / d));
        System.out.println("c % d = " +(c % d));


    }
}
